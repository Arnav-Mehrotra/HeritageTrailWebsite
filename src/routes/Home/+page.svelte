			<div>
				<h2>Improvements Needed for the Heritage Trail</h2>
				<p>
					The Heritage Trail is the only unimproved multi-use trail in the Town of
					Niagara-on-the-Lake’s Official Plan. It dissects the epicentre of the Wine Route in NOTL,
					a key destination for cycling, eco-tourism and agri-tourism.As the automobile was
					responsible for the decline of the railway, it seems fitting that a shift from the
					automobile to a renewed priority on active transportation has the potential to breathe new
					life back into the Heritage Trail.
				</p>

				<p>
					The 66-foot-wide corridor is visibly broken and even unknown to many residents of
					Niagara-on-the-Lake. Utilization of the trail has been compromised in the last few years
					by erosion and washout between Line 9 and York Road, and in other sections.
				</p>

				<p>
					The overall integrity of the corridor is eroding due to an inability for people to use it.
					Vegetation and grass are growing where people used to walk, run, cycle and ride horses.
					The trail is losing its “visibility” in both a physical sense and in the overall
					consciousness of the Town of Niagara-on-the-Lake.
				</p>

				<p>
					The NOTL Canada Sesquicentennial Committee, created in 2017 to support and execute events
					to commemorate Canada's 150 years of Confederation, chose the rehabilitation of the
					Heritage Trail as its Legacy project, to connect communities and to preserve a piece of
					Canadian history for new generations. A committee of the Town of Niagara-on-the-Lake, The
					Heritage Trail, was initiated in 2019 and will be directed by a group of volunteers from
					the community.
				</p>
