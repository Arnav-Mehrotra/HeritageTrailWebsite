				standards.
			</p>
			<p class="tracking-wide leading-7 text-shadow">
				Removal of brush and undergrowth will allow for the best location of the trail. Planting of
				appropriate native trees and bushes will be undertaken where needed.
			</p>
			<p class="tracking-wide leading-7 text-shadow">
				Drainage improvements will be required in some locations to ensure stability and prevent
