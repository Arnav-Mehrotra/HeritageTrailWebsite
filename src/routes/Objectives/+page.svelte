			<p class="tracking-wide leading-7 text-shadow">
				The Heritage Trail follows the original railway corridor of the Erie and Ontario Railway,
				later the Michigan Central, which connected the Town of Niagara (now Niagara on the Lake) to
				Niagara Falls and the Great Western Railway.
			</p>
			<p class="tracking-wide leading-7 text-shadow">
				When operations began in 1854 it was one of the first steam-powered railroads in the
				province. Following the opening of the second Welland Canal in 1840, the Town entered a
				recession and the railroad brought a much-needed boost to the local economy. It played a
				vital role during World War 1, bringing supplies and troops to the army's Niagara Camp.
			</p>
			<p class="tracking-wide leading-7 text-shadow">
				For decades, the train was important to the community providing transportation links for the
				tourism, agricultural and fishing industries. The railroad ceased operations in 1959,
				leaving behind a twenty metre wide right-of-way that began to be used by equestrians,
				hikers, and motorized sport vehicles. Part of the vision of the Heritage Trail Committee is
				to commemorate the history of the railroad and its importance to a growing community.
			</p>

